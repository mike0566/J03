#include <stdio.h>

void	ft_swap(int *a, int *b)
{
	int		c = *a;
	*a = *b;
	*b = c;
}

int		main(void)
{
	int		a = 2;
	int		b = 4;
	ft_swap(&a, &b);
	printf("%d%d", a, b);
	return (0);
}
